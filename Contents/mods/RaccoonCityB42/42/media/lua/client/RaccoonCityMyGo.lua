-- 播放3D音效
function RaccoonCityMyGo.play3DSound(_playerNum, tSquare, soundName)
	if RaccoonCityMyGo.Audio then 
		getSoundManager():StopSound(RaccoonCityMyGo.Audio) 
	end 
	RaccoonCityMyGo.Audio = getSoundManager():PlayWorldSound(soundName, tSquare, 1, 5, 1, false)
end

-- 停止
function RaccoonCityMyGo.onClickMenuStop(this)
	if RaccoonCityMyGo.Audio then 
		getSoundManager():StopSound(RaccoonCityMyGo.Audio) 
	end 
	RaccoonCityMyGo.Audio = nil
end 

