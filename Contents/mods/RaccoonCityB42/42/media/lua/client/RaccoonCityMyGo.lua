function RaccoonCityMyGo.MyGoRightMenu(_playerNum, _context, _worldObjects,strSprite,tSquare)
	if strSprite and strSprite ~= '' then 
		if RaccoonCityMyGo.SpriteList[strSprite] then 
			local menuOption = _context:addOption(getText(RaccoonCityMyGo.SpriteList[strSprite].Name))
			local subMenu = ISContextMenu:getNew(_context)
			_context:addSubMenu(menuOption, subMenu)
			subMenu:addOption(getText('ContextMenu_Random'), _worldObjects, RaccoonCityMyGo.onClickMenuRandom, _playerNum, strSprite, tSquare)
			for k,v in ipairs(RaccoonCityMyGo.SpriteList[strSprite]) do
				subMenu:addOption(getText(v.MenuName), _worldObjects, RaccoonCityMyGo.onClickMenu, _playerNum, strSprite, k, tSquare)
			end 
			-- 唱歌
			if RaccoonCityMyGo.SpriteList[strSprite].SingName then 
				local menuOption2 = _context:addOption(getText(RaccoonCityMyGo.SpriteList[strSprite].SingName))
				local subMenu2 = ISContextMenu:getNew(_context)
				_context:addSubMenu(menuOption2, subMenu2)
				for k,v in ipairs(RaccoonCityMyGo.SpriteList[strSprite].SongList) do
					subMenu2:addOption(getText(v.MenuName), _worldObjects, RaccoonCityMyGo.onClickMenuSing, _playerNum, strSprite, k, tSquare)
				end 
			end 
			-- 停止
			_context:addOption(getText('ContextMenu_StopSound'), _worldObjects, RaccoonCityMyGo.onClickMenuStop, _playerNum)
		end 
	end
end 


