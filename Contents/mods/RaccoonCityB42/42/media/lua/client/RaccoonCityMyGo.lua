-- 随机
function RaccoonCityMyGo.onClickMenuRandom(this, _playerNum, strSprite, tSquare)
	local ran = ZombRand(#RaccoonCityMyGo.SpriteList[strSprite]) + 1
	local strSound = RaccoonCityMyGo.SpriteList[strSprite][ran].SoundName
	RaccoonCityMyGo.play3DSound(_playerNum, tSquare, strSound)
end

-- 固定
function RaccoonCityMyGo.onClickMenu(this, _playerNum, strSprite, index,tSquare)
	local strSound = RaccoonCityMyGo.SpriteList[strSprite][index].SoundName
	RaccoonCityMyGo.play3DSound(_playerNum, tSquare, strSound)
end 

-- 唱歌
function RaccoonCityMyGo.onClickMenuSing(this, _playerNum, strSprite, index,tSquare)
	local strSound = RaccoonCityMyGo.SpriteList[strSprite].SongList[index].MusicName
	RaccoonCityMyGo.play3DSound(_playerNum, tSquare, strSound)
end 

